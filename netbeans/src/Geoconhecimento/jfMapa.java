    private void jPanel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseClicked
        if(!loaded) return;
        
        int ctrldown = CTRL_DOWN_MASK;
        
        if ( evt.getButton() == BUTTON3 || (evt.getModifiersEx() & CTRL_DOWN_MASK) == CTRL_DOWN_MASK) {
            // calcular caminho
            mapa.caminhoMaisCurto(geoconhecimento, tModel, evt.getX(), evt.getY());
        }else{
            // clique simples
            mapa.updateTable(geoconhecimento, tModel, evt.getX(), evt.getY());
        }
        
        
    }//GEN-LAST:event_jPanel1MouseClicked

    private void jPanel1MouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseDragged
        if(!loaded) return;
        
        Figura.mouseMoved(mouseDownX, mouseDownY, evt.getX(), evt.getY());
        mouseDownX = evt.getX();
        mouseDownY = evt.getY();
        mapa.desenharTudo();
    }//GEN-LAST:event_jPanel1MouseDragged

    private void jPanel1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MousePressed
        if(!loaded) return;
        
        mouseDownX = evt.getX();
        mouseDownY = evt.getY();
    }//GEN-LAST:event_jPanel1MousePressed

